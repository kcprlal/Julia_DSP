function rozwiazanie(;
    x::Vector{Float64} = [-4.01, -4.1, 4.2, 0.82, 1.19, -2.7, -1.73, 3.1, -0.04, 0.82, 3.61, 0.69, -4.71, 4.96, -0.31, -1.18, 3.8, 4.1, 2.38, 0.59, 4.19, 0.59, 1.52, -4.85, 1.16, 1.94, -0.77, 3.4, -2.46, -3.3, 4.23, 3.58, 1.87, -2.82, 4.51, -4.04, 4.05, 0.26, -2.44, 0.18, 2.01, -0.34, 4.1, -1.02, 2.86, 1.31, 0.3, -4.51, -4.77, -2.88, 1.69, -2.05, 4.97, 1.45, -4.34, 2.43, 3.62, 0.72],
    h::Vector{Float64} = [-4.27, -1.66, -4.18, -1.83, -1.46, -2.58, -2.05, -3.54, 0.56, -1.55, -0.31, -0.98, 1.34],
)
    m=length(x)
    n=length(h)
    y=zeros(Float64,m+n-1)
    
